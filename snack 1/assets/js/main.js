let firstNumber = parseInt(prompt('1) Inserisci il primo numero'));
let secondNumber = parseInt(prompt('2) inserisci il secondo numero'));


if (firstNumber > secondNumber) {
    console.log('Il secondo numero è il maggiore: ' + firstNumber)
} 
else if (firstNumber < secondNumber) {
    console.log('Il secondo numero è il maggiore: ' + secondNumber)
}
else {
    console.log('I due numeri sono uguali')
}